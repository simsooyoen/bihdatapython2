# 한 달 식단을 추천하는 함수
def recommend_monthly_diet(goal):
    # 목표에 맞는 식단 데이터 확인
    goal = goal.strip()
    if goal not in diet_plans:
        print("목표가 정확하지 않습니다. '체중 감량' 또는 '근육 증가'로 입력해주세요.")
        return
    
    print(f"당신의 목표는 '{goal}'입니다. 한 달 식단을 추천해드릴게요!\n")
    
    # 하루하루 식단을 랜덤으로 선택하여 출력
    for day in range(1, 31):
        print(f"\n[Day {day}]")
        breakfast = random.choice(diet_plans[goal]["아침"])
        lunch = random.choice(diet_plans[goal]["점심"])
        dinner = random.choice(diet_plans[goal]["저녁"])
        
        print(f"  아침 : {breakfast}")
        print(f"  점심 : {lunch}")
        print(f"  저녁 : {dinner}")

# 사용자 입력 받기
print("당신의 다이어트 목표는 무엇인가요? (체중 감량 / 근육 증가)")
goal = input("목표를 입력해주세요: ")

# 다이어트 식단 추천
recommend_monthly_diet(goal)